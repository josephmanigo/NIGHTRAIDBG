export const ACHIEVEMENT_FILTERS = ['All', 'Championships', 'Podium Finishes', 'Special'] as const
export type AchievementFilter = (typeof ACHIEVEMENT_FILTERS)[number]

export function filterAchievements(filter: AchievementFilter): Achievement[] {
  switch (filter) {
    case 'Championships':
      return ACHIEVEMENTS.filter((a) => a.category === 'Championship')
    case 'Podium Finishes':
      return ACHIEVEMENTS.filter((a) => a.category === 'Podium')
    case 'Special':
      return ACHIEVEMENTS.filter((a) => a.category === 'Special')
    default:
      return ACHIEVEMENTS
  }
}
